}

</style>

<div class='header'>

<div class='company'>
<h2>GiftShop</h2>
<div>201/A, Maninagar, Ahmedabad</div>
<div>Email: giftshopmaninagar@gmail.com | Phone: 9876543210</div>
</div>

</div>

<h3>$reportName</h3>

<div class='meta'>
Generated : ".date("d M Y h:i A")."
</div>

<div class='summary'>
<b>Total Products Sold :</b> $grandTotal
</div>

<table>

<thead>

<tr>
<th>Product ID</th>
<th>Product Name</th>
<th>Total Sold</th>
</tr>

</thead>

<tbody>

$rows

</tbody>

<tfoot>

<tr>

<td colspan='2' style='text-align:right;font-weight:bold;background:#f8f3ee'>
Grand Total Sold
</td>

<td style='font-weight:bold;background:#f8f3ee'>
$grandTotal
</td>

</tr>

</tfoot>

</table>

<div class='footer'>
Generated by GiftShop Admin Panel
</div>

";

/* ================= GENERATE PDF ================= */

$options=new Options();
