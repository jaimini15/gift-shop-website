<?php
date_default_timezone_set("Asia/Kolkata");

require_once '../../dompdf/autoload.inc.php';
use Dompdf\Dompdf;
use Dompdf\Options;

include("../db.php");

/* ================= GET FILTER VALUES ================= */

$type  = $_GET['type'] ?? '';
$limit = $_GET['limit'] ?? '';

/* ================= REPORT NAME ================= */

$reportName="Top / Least Selling Products Report";

if($type=="top"){
$reportName="Top Selling Products Report";
}

if($type=="low"){
$reportName="Least Selling Products Report";
}

/* ================= QUERY ================= */

$query="

SELECT 
c.Category_Name,
p.Product_Id,
p.Product_Name,
SUM(oi.Quantity) AS Total_Sold

FROM order_item oi

JOIN product_details p 
ON oi.Product_Id = p.Product_Id

JOIN category_details c 
ON p.Category_Id = c.Category_Id

GROUP BY p.Product_Id

";

/* FILTER */

if($type=="top" && $limit){
$query.=" ORDER BY Total_Sold DESC LIMIT $limit";
}
elseif($type=="low" && $limit){
$query.=" ORDER BY Total_Sold ASC LIMIT $limit";
}
else{
$query.=" ORDER BY c.Category_Name,p.Product_Name";
}

$result=mysqli_query($connection,$query);

/* ================= BUILD TABLE ================= */

$currentCategory="";
$rows="";
$categoryTotal=0;
$grandTotal=0;

while($row=mysqli_fetch_assoc($result)){

if($currentCategory!=$row['Category_Name']){

if($currentCategory!=""){

$rows.="
<tr class='category-total'>
<td colspan='2'>Category Total</td>
<td>$categoryTotal</td>
</tr>
";

$categoryTotal=0;
}

$currentCategory=$row['Category_Name'];

$rows.="

<tr class='category-row'>
<td colspan='3'>
Category : $currentCategory
</td>
</tr>

";
}

$rows.="

<tr>

<td>{$row['Product_Id']}</td>
<td>{$row['Product_Name']}</td>
<td>{$row['Total_Sold']}</td>

</tr>

";

$categoryTotal+=$row['Total_Sold'];
$grandTotal+=$row['Total_Sold'];

}

/* LAST CATEGORY TOTAL */

if($currentCategory!=""){

$rows.="

<tr class='category-total'>
<td colspan='2'>Category Total</td>
<td>$categoryTotal</td>
</tr>

";

}

/* ================= PDF HTML ================= */

$html="

<style>

@page{
margin:15px 20px 70px 20px;
}

body{
font-family:DejaVu Sans,sans-serif;
font-size:12px;
